  ## Modern Science

  * 1940s experiment shown that carbon dioxide did block the
  transmission of infrared ‘long-wave’ radiation.  experiments showed there was very little
  change in this interception if the amount of carbon dioxide was
  doubled or halved
  * This work was brought together
  in 1955 by the calculations of Gilbert Plass, who concluded that
  adding more carbon dioxide to the atmosphere would intercept
  more infrared radiation, preventing it being lost to space and thus
  warming the planet.


  * argument that the oceans would soak up the extra
  anthropogenically produced carbon dioxide.
  * 1950s and showed that the average lifetime of a carbon
  dioxide molecule in the atmosphere before it dissolved in the sea
  was about ten years.
  *  Roger Revelle: Did it stay there or diffuse back into the atmosphere? How much
  extra CO2 could the oceans hold? Revelle’s calculations showed that
  the complexities of the surface ocean chemistry are such that it
  returns much of the carbon dioxide that it absorbs.the exact amount of anthropogenic carbon
  dioxide taken up per year by the oceans is still in debate. It is
  thought to be about 2 gigatonnes, nearly a third of the annual total
  anthropogenic production.
  * Charles Keeling: late 1950s and early 1960s Keeling used the most modern technology available to measure the concentration of atmospheric CO2 in Antarctica and Mauna Loa. The resulting Keeling CO2 curves have continued to climb ominously each year since the first measurement in 1958 and have become one of the major icons of global warming.
  * Physical geosciences being favoured financially
in the Cold War environment (Spencer Weart)
  * Gilbert Plass published an article in 1959 in Scientific American declaring that the world’s temperature would rise by 3°C by the end of  the century.

  * Why did it take so long?
    * From 1940 till the mid-1970s the global temperature curve
seems to have had a general downward trend (We now know that the cooling trend of the 1970s is
due to the decadal influence of the sunspot cycle.)
    * discuss whether the Earth was entering the
next great ice age.
    * early 1980s, when the global annual mean
temperature curve started to increase, that the global cooling
scenario was questioned
    * e late 1980s the global annual mean
temperature curve rose so steeply that all the dormant evidence
from the late 1950s and 1960s was given prominence and the global
warming theory was in full swing. 
  * growing environmental awareness:
    * Rachel Carson’s Silent Spring in 1962 
    * Image of Earth seen from the moon in 1969
    * Club of Rome’s 1972 report on Limits to Growth
    * Three Mile Island nuclear reactor accident in 1979, the nuclear accident at Chernobyl in 1986
    * Exxon Valdez oil spillage in 1989
  *  Ozone ‘hole’:
    * 1985 by the British Antarctic Survey of depletion of ozone over Antarctica which demonstrated the global connectivity of our environment.
    * The 1985 Vienna Convention for the Protection of the Ozone Layer and 1987 Montreal Protocol on Substances that Deplete the Ozone layer and 1990 London and 1992 Copenhagen Adjustments and Amendments to the Protocol

* So by combining (1) the science of global warming essentially
carried out by the mid-1960s, (2) the frightening upturn in the
global temperature data set at the end of the 1980s, (3) our
increased knowledge of how past climate has reacted to changes
in atmospheric carbon dioxide in the 1980s, (4) the emergence
of the global environmental awareness in the late 1980s, and
(5) the media’s savage interest in the confrontational nature of the
debate, we are led to the final recognition of the global warming
hypothesis.

