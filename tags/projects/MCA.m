## Description


In the realm of climate science, Earth System Models (ESMs) stand as pivotal tools for comprehending climate evolution, assessing historical climates, and projecting future scenarios. Originating in the 1950s alongside the advent of supercomputers, these models have evolved from rudimentary atmospheric predictions to intricate simulations encompassing ocean circulation, radiation balance, and the influence of human-induced CO2 emissions. The Intergovernmental Panel for Climate Change (IPCC), established in 1988, underlines the significance of ESMs in comprehending the ramifications of global warming.

ESMs are intricate constructs, defined by partial differential equations representing processes like fluid mechanics, thermodynamics, and biochemical interactions. However, these models grapple with computational constraints that hinder accurate representation of small-scale phenomena such as cloud formation and plankton circulation. Moreover, the dynamic nature of human-induced climate changes compounds the challenges of modeling and prediction.

Recently, statistical methodologies have gained ground in climate research, ushering in a new era of data analysis. Though fields like health sciences and economics swiftly embraced statistical tools, climate science largely adhered to early 20th-century techniques like Principal Components Analysis and linear regression. Nonetheless, the surge in climate data, satellite observations, and the rise of Machine Learning have revitalized statistical analysis in this domain.

Amidst this landscape, causal analysis emerges as a potent avenue for unraveling the complexities of Earth systems. Bridging statistics and computer science, causal analysis encounters hurdles in the non-stationary and chaotic nature of climate dynamics, as well as the challenge of reconciling the causal framework with energy conservation principles.

This project delves into a pivotal concern at the intersection of causal discovery, causal representation learning, and causal inference: the identification of latent confounders from high-dimensional proxy variables. This pursuit aims to enable unbiased estimation of causal effects, contributing to a more accurate understanding of how Earth system variables interact.

In its early stages, this research outlines significant challenges, reviews existing methodologies within similar contexts, and charts a course for addressing prevalent limitations. By leveraging causal analysis, this work aspires to illuminate the intricate interplay between Earth's systems, human activities, and policy interventions in the face of climate change.

